#pragma once

struct startup
{
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // COMMANDS
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  struct initiate
  {};

  struct complete
  {};

  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // EVENTS
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  struct initiated
  {};

  struct completed
  {};

  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // STATE
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  struct state_type
  {};

  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // EXECUTE
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  static initiated execute(const state_type&, const initiate&)
  {
    return initiated{};
  }

  static completed execute(const state_type&, const complete&)
  {
    return completed{};
  }

  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
  // PROCESSES
  //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  static complete process(const state_type&, const initiated&)
  {
    return complete{};
  }
};
